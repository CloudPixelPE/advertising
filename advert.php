                            <p align="justify">
Online advertising is a form of promotion that uses the Internet and World Wide Web for the expressed purpose of delivering marketing messages to attract customers. Online ads are delivered by an ad server. <br>Examples of online advertising include contextual ads that appear on search engine results pages, banner ads, in pay per click text ads, rich media ads, Social network advertising, online classified advertising, advertising networks and e-mail marketing, including e-mail spam. <br>A newer form of online advertising is Native Ads; they go in a website's news feed and are supposed to improve user experience by being less intrusive. </p>


